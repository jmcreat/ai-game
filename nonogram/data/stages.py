def get_all_stages() -> list:
    """모든 스테이지 Puzzle 객체 반환"""
    puzzles = []
    for s in STAGE_DATA:
        p = Puzzle(s["solution"], title=s["title"])
        p.stage_id = s["id"]
        p.difficulty_label = s["difficulty"]
        puzzles.append(p)
    return puzzles


def get_stage(stage_id: int) -> Puzzle:
    for s in STAGE_DATA:
        if s["id"] == stage_id:
            p = Puzzle(s["solution"], title=s["title"])
            p.stage_id = s["id"]
            p.difficulty_label = s["difficulty"]
            return p
    raise ValueError(f"Stage {stage_id} not found")
